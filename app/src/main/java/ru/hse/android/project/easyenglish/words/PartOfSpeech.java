package ru.hse.android.project.easyenglish.words;

/**
 * Part of speech for Extended word.
 * It is used in WordChain to check if a word is a noun.
 */
public enum PartOfSpeech {
    NOUN,
    VERB,
    ADJECTIVE,
    ADVERB,
    PRONOUN,
    PREPOSITION,
    CONJUCTION,
    INTERJECTION,
    ARTICLE,
    DETERMINER
}
